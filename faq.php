        <h3 class="text-white font-semibold text-lg">What is the Metro Weather – Automated Weather Station (AWS)
            Project?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">Metro Weather is a public-private partnership
            that has established a network of 30 automated weather stations (AWS) in Metro Manila. This network will
            provide free and near real-time weather data that can be used to prepare for severe weather conditions such
            as tropical cyclones and heavy flooding. The weather stations are spread over an approximate area of 625
            square kilometers, and are distributed so that each station covers roughly 25 square kilometers.</p>

        <h3 class="text-white font_semibold text-lg">Who are the Project Partners/Proponents?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">The project partners are the Metro Manila
            Development Authority (MMDA), Chevron, the Manila Observatory (MO), Globe Telecom, and the Ateneo de Manila
            University (ADMU).</p>

        <h3 class="text-white font_semibold text-lg">What are the roles of the Project Partners/Proponents?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">Chevron, through its business units (Chevron
            Philippines, Inc., Philippine Geothermal Production Company, Chevron Holdings, Inc. Chevron Malampaya LLC)
            have partnered to provide both the financial resources as well as technical and infrastructure support
            through select Caltex service stations in strategic locations. These stations shall host the AWS to provide
            Metro Manila residents with real-time weather monitoring as part of its commitment to safety and climate
            change adaptation.</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">Manila Observatory conceptualized the
            establishment of a dense, urban network of AWS. MO provides the scientific and technical support in the
            installation, operation and maintenance of the AWS network. It participates in the public communication of
            the weather data. More importantly, MO analyzes the data in relation to potential disaster risk and provides
            research opportunities in urban disaster risk reduction and climate change.</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">Globe Telecom, through its corporate social
            responsibility arm, Globe Bridging Communities, seeks partnerships that support its advocacy of
            environmental sustainability. By providing ICT infrastructure, Globe hopes to promote awareness of
            environmental issues to its employees and stakeholders, and prompt them to participate in activities that
            help conserve the environment and mitigate climate change.</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">MMDA assisted in the selection of appropriate
            locations for the AWS, will support MO in providing information which will aid the development and
            implementation of an evidenced-based decision support system for urban risk reduction, and assist in the
            physical maintenance of the AWS.</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">Ateneo, through the development of opportunities
            for student research in atmospheric physics, instrumentation development, and disaster risk reduction,
            assists in creating relevant academic and research entry points for the analysis and communication of the
            AWS data.</p>

        <h3 class="text-white font_semibold text-lg">Which areas will be covered by the Metro Weather Project?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">The 30 AWS are deployed in major Metro Manila
            cities, and strategic areas like Antipolo City, Rodriguez, Angono and Jala-jala in Rizal.</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">The AWS are placed in sites hosted by the Metro
            Manila Development Authority (MMDA), Chevron through its various Caltex retail stations around Metro Manila,
            and at Globe Telecom facilities. They are located in critical areas identified by the MMDA, from the upper
            Marikina watershed to the flood-prone sections of Metro Manila.</p>

        <h3 class="text-white font_semibold text-lg">What is the Implementation Plan?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">Data transmission from thirty stations will be
            powered by Globe, and sent to the MMDA through the Manila Observatory and the Ateneo de Manila University.
            These can be accessed freely via www.metroweather.com.ph, weatherlink.com and MMDA's app, “Metro Manila
            Traffic Navigator.”</p>

        <h3 class="text-white font_semibold text-lg">Is there a cost to access the information?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">There will be no cost to access the information
            through the websites and the MMDA app, “Metro Manila Traffic Navigator.”</p>

        <h3 class="text-white font_semibold text-lg">What kind of information will I get from Metro Weather?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">The Metro Weather stations will collect and
            store information such as rainfall, temperature, relative humidity, wind speed and direction, barometric
            pressure, solar radiation and ultraviolet index.</p>

        <h3 class="text-white font_semibold text-lg">How useful will this information be?</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">The near real-time weather data can be used to
            prepare for severe weather conditions such as tropical cyclones and heavy flooding. The data collected at
            these sites will also provide critical information for ongoing research on urban climate change and
            variability.</p>

        <h3 class="text-white font_semibold text-lg">From the project partners:</h3>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">"Mitigating climate change and its effects
            requires a concerted effort from the private and public sectors," said Chevron Country Chairman Jim Meynink.
            "This project represents what both can accomplish, which will ultimately help Metro Manila residents survive
            disasters caused by severe weather."</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">"This system will provide information on
            significant weather events as a supplement to weather observations of Metro Manila. It is hoped that this
            privately initiated joint effort will also support education and research on urban disaster risk reduction
            in light of our changing climate," said MO Executive Director Antonia Yulo Loyzaga.</p>
        <p class="m-3 text-gray-900 text-justify font-medium text-base">"Metro Weather will also help save more lives on
            the road," said MMDA Chairman Francis Tolentino, adding that Metro Weather's app, “Metro Manila Traffic
            Navigator,” will help motorists determine alternative routes during times of torrential rain and flooding.
